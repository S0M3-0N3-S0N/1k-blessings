const LANGUAGES = [
  { code: "en", flag: "🇺🇸", native: "English", english: "English" },
  { code: "es", flag: "🇪🇸", native: "Español", english: "Spanish" },
  { code: "fr", flag: "🇫🇷", native: "Français", english: "French" },
  { code: "zh", flag: "🇨🇳", native: "中文", english: "Chinese" },
];

const LanguageContext = createContext(null);

export function LanguageProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem("1kb-lang") || "en");

  const setLanguage = useCallback((code) => {
    setLang(code);
    localStorage.setItem("1kb-lang", code);
  }, []);

  const t = useCallback((key) => {
    return TRANSLATIONS[lang]?.[key] || TRANSLATIONS["en"]?.[key] || key;
  }, [lang]);

  return (
    <LanguageContext.Provider value={{ lang, setLanguage, t, LANGUAGES }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error("useLanguage must be used within LanguageProvider");
  return ctx;
}